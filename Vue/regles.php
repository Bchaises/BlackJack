		<h4>STRATÉGIE DE BASE</h4>
		<p>
			Certes le Blackjack est un jeu de hasard mais il est possible d’influer sur la partie avec une stratégie basée sur l’observation dont les principes les plus simples sont :
			- Si le croupier a une carte comprise entre 2 et 6, il ne peut pas gagner puisque même avec une « bûche », il sera en-dessous de 17 et il devra tirer une carte supplémentaire
			- Si le croupier tire un 7, attention car s’il sort une « bûche » il sera en mesure de gagner.
 

			Avec la pratique, vous apprendrez à anticiper les possibilités du jeu du Croupier et vos chances de gagner.

			Lancez-vous et décidez quand vous arrêter, doubler la mise ou tirer deux cartes égales
